#include <stdio.h>

void main()
{
	int a = 10;

	a++;
	printf(" a ++ ==> %d \n", a); //a = a + 1苞 悼老
	a--;
	printf(" a -- ==> %d \n", a); //a = a - 1苞 悼老
	a += 5;
	printf(" a +=5 ==> %d \n", a); //a = a + 5客 悼老
	a  -= 5;
	printf(" a -=5 ==> %d \n", a); //a = a - 5客 悼老
	a *= 5;
	printf(" a *=5 ==> %d \n", a); //a = a * 5客 悼老
	a /= 5;
	printf(" a /=5 ==> %d \n", a); //a = a / 5客 悼老
	a %= 5;
	printf(" a %%=5 ==> %d \n", a); //a = a % 5客 悼老
}
